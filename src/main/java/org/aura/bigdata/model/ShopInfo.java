package org.aura.bigdata.model;

public class ShopInfo {

    private String shop_id = null;
    private String city_name = null;
    private String location_id = null;
    private Long per_pay = null;
    private String score = null;
    private Integer comment_cnt = null;
    private Integer shop_level = null;
    private String cate_1_name = null;
    private String cate_2_name = null;
    private String cate_3_name = null;

    public String getShop_id() {
        return shop_id;
    }

    public void setShop_id(String shop_id) {
        this.shop_id = shop_id;
    }

    public String getCity_name() {
        return city_name;
    }

    public void setCity_name(String city_name) {
        this.city_name = city_name;
    }

    public String getLocation_id() {
        return location_id;
    }

    public void setLocation_id(String location_id) {
        this.location_id = location_id;
    }

    public Long getPer_pay() {
        return per_pay;
    }

    public void setPer_pay(Long per_pay) {
        this.per_pay = per_pay;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }

    public Integer getComment_cnt() {
        return comment_cnt;
    }

    public void setComment_cnt(Integer comment_cnt) {
        this.comment_cnt = comment_cnt;
    }

    public Integer getShop_level() {
        return shop_level;
    }

    public void setShop_level(Integer shop_level) {
        this.shop_level = shop_level;
    }

    public String getCate_1_name() {
        return cate_1_name;
    }

    public void setCate_1_name(String cate_1_name) {
        this.cate_1_name = cate_1_name;
    }

    public String getCate_2_name() {
        return cate_2_name;
    }

    public void setCate_2_name(String cate_2_name) {
        this.cate_2_name = cate_2_name;
    }

    public String getCate_3_name() {
        return cate_3_name;
    }

    public void setCate_3_name(String cate_3_name) {
        this.cate_3_name = cate_3_name;
    }
}
